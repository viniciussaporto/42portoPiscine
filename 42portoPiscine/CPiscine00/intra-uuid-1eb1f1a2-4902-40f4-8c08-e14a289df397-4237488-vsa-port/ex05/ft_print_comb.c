#include <unistd.h>

void	ft_print_comb(void)
{
	char	d[3];

	d[0] = '0';
	while (d[0] <= '7')
	{
		d[1] = d[0] + 1;
		while (d[1] <= '8')
		{
			d[2] = d[1] + 1;
			while (d[2] <= '9')
			{
				write(1, &d[0], 1);
				write(1, &d[1], 1);
				write(1, &d[2], 1);
				{
					if (d[0] != '7')
						write(1, ", ", 2);
				}
				d[2]++;
			}
			d[1]++;
		}
		d[0]++;
	}
}
